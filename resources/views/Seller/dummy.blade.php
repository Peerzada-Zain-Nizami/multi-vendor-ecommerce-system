    function noti() {
        $.ajaxSetup({
            headers: {
                'X-CSRF-TOKEN': $('meta[name="csrf-token"]').attr('content')
            }
        });
        $.ajax({
            url: "{{route('seller.get.noti')}}",
            type: "GET",
            dataType: "json",
            success: function (data) {
                if (data.new == "true") {
                    $('#pulse').addClass('pulse');
                }
                if (data.new == "false") {
                    $('#pulse').removeClass('pulse');
                }
                $('#notification_body').html(data.body);
                sound(data.arrival);
            }
        })
    }

    noti();
    setInterval(noti, 3000);

    function see(id) {
